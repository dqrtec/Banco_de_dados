public class Album {
    
   int idAlbum;
   int codigoGravadora;
   float precoCompra;
   String tipoCompra;
   String dataGravacao;
   String descricao;
   String dataCompra;

    public int getIdAlbum() {
        return idAlbum;
    }

    public void setIdAlbum(int idAlbum) {
        this.idAlbum = idAlbum;
    }

    public int getCodigoGravadora() {
        return codigoGravadora;
    }

    public void setCodigoGravadora(int codigoGravadora) {
        this.codigoGravadora = codigoGravadora;
    }

    public float getPrecoCompra() {
        return precoCompra;
    }

    public void setPrecoCompra(float precoCompra) {
        this.precoCompra = precoCompra;
    }

    public String getTipoCompra() {
        return tipoCompra;
    }

    public void setTipoCompra(String tipoCompra) {
        this.tipoCompra = tipoCompra;
    }

    public String getDataGravacao() {
        return dataGravacao;
    }

    public void setDataGravacao(String dataGravacao) {
        this.dataGravacao = dataGravacao;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getDataCompra() {
        return dataCompra;
    }

    public void setDataCompra(String dataCompra) {
        this.dataCompra = dataCompra;
    }
   
   
   
}
